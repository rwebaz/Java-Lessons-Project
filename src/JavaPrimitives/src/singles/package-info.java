package singles;

/* There are two types of 'single' primitives in Java
 * Boolean (true or false) and, 
 * char
 * The Boolean values hold single digit members of the binary framework (1) for 'true' and (0) for false.
 * The 'char' variables hold single members of the 'unicode' character framework.
 * The 'char' variable is enclosed in single quotes.
 * Examples of the 'char' variable are, as follows:
 * char Var = '\t' // the 'tab' character
 * char Var = '\r\ // the 'carriage return' characters
 * char Var = '\n' // the 'line-feed' character
 * char Var = 'x' // the 'small x' character
 * * @author rwebaz */